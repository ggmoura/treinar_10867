package br.com.treinar.java_basico.estudo.datatypes;

public class DataType {

	public static void main(String[] args) {
		byte a = 100;
		System.out.println(a);
		short s = 10000;
		System.out.println(s);
		int i = 100000;
		System.out.println(i);
		long l = 100000;
		System.out.println(l);
		float f1 = (float) 234.5;
		System.out.println(f1);
		double d1 = 1.2;
		System.out.println(d1);
		boolean possuiFichaCriminal = true;
		System.out.println("A Pessoa possui ficha criminal: " + possuiFichaCriminal);
		char letterA = 'A';
		System.out.println(letterA);
		char letterB = 32;
		System.out.println(letterB);
		System.out.println("Gleidson\nMoura");
	}

}
